package com.example.demo.model;

import java.util.Date;

public class dateobject {
	private Date date;
	
	
	
	
	public dateobject() {
		super();
	}
	
	
	

	public dateobject(Date date) {
		super();
		this.date = date;
	}


	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}
